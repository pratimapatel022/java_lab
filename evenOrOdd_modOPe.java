public class evenOrOdd_modOPe {
    public static void main(String[] args) {
        int number = 7;

        // Using modulus operator to determine if the number is even or odd
        if (number % 2 == 0) {
            System.out.println(number + " is an even number.");
        } else {
            System.out.println(number + " is an odd number.");
        }
        
    }
    
}
